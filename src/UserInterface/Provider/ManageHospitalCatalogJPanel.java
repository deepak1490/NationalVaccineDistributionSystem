    private void refreshTable() {
        int rowCount = hospitalCatalog.getRowCount();
        DefaultTableModel model = (DefaultTableModel) hospitalCatalog.getModel();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        for (Hospital h : providerEnterprise.getHospitalDirectory().getHospitalList()) {
            Object row[] = new Object[3];
            row[0] = h;
            row[1] = h.getHospitalID();
            row[2] = h.getHospitalAddress();
            model.addRow(row);
        }
    }

