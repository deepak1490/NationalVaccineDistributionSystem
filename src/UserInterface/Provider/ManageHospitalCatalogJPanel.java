    private void viewPButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPButtonActionPerformed
        // TODO add your handling code here:
        int row = hospitalCatalog.getSelectedRow();
        int selectedRowCount = hospitalCatalog.getSelectedRowCount();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a hospital to view the details");
            return;
        }
        if (selectedRowCount > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one hospital");
        }
        Hospital h = (Hospital) hospitalCatalog.getValueAt(row, 0);
        ViewHospitalDetailJPanel vpdjp = new ViewHospitalDetailJPanel(userProcessContainer, h);
        userProcessContainer.add("ViewHospitalDetailJPanel", vpdjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewPButtonActionPerformed

    private void createPButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createPButtonActionPerformed

        JTextField hospitalNameTextField = new JTextField();
        JTextArea hospitalAddressTextArea = new JTextArea();

        Object[] addHospital = {"Hospital Name:", hospitalNameTextField, "Hospital Address:", hospitalAddressTextArea};
        int option = JOptionPane.showConfirmDialog(null, addHospital, "Add Hospital", JOptionPane.OK_CANCEL_OPTION, -1);

        if (option == JOptionPane.YES_OPTION) {
            Hospital hospital = providerEnterprise.getHospitalDirectory().addHospital();
            if (hospitalNameTextField.getText() == null || hospitalAddressTextArea.getText() == null) {
                JOptionPane.showMessageDialog(null, "Please provide a valid entry");
                return;

            }
            try {
                String name = hospitalNameTextField.getText();
                String address = hospitalAddressTextArea.getText();
                hospital.setHospitalName(name);
                hospital.setHospitalAddress(address);
                hospital.setAdmin("Unassigned");
                hospital.setFinanaceUser("Unassigned");
                hospital.setInventoryUser("Unassigned");
                hospital.setOrderUser("Unassigned");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please provide a valid entry");
                return;
            }
            JOptionPane.showMessageDialog(null, "Hospital details added successfully");
            refreshTable();
        }

    }//GEN-LAST:event_createPButtonActionPerformed

