    private void refreshTable() {
        int rowCount = pharmacyCatalog.getRowCount();
        DefaultTableModel model = (DefaultTableModel) pharmacyCatalog.getModel();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        for (Pharmacy p : providerEnterprise.getPharmacyDirectory().getPharmacyDirectory()) {
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getPharmacyID();
            row[2] = p.getPharmacyAddress();
            model.addRow(row);
        }
    }

