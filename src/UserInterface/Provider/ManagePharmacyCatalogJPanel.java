    private void viewPButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPButtonActionPerformed
        // TODO add your handling code here:
        int row = pharmacyCatalog.getSelectedRow();
        int selectedRowCount = pharmacyCatalog.getSelectedRowCount();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a pharmacy to view the details");
            return;
        }
        if (selectedRowCount > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one pharmacy");
        }
        Pharmacy p = (Pharmacy) pharmacyCatalog.getValueAt(row, 0);
        ViewPharmacyDetailJPanel vpdjp = new ViewPharmacyDetailJPanel(userProcessContainer, p);
        userProcessContainer.add("ViewHospitalDetailJPanel", vpdjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewPButtonActionPerformed

    private void createPButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createPButtonActionPerformed

        JTextField pharmacyNameTextField = new JTextField();
        JTextArea pharmacyAddressTextArea = new JTextArea();

        Object[] addPharmacy = {"Pharmacy Name:", pharmacyNameTextField, "Pharmacy Address:", pharmacyAddressTextArea};
        int option = JOptionPane.showConfirmDialog(null, addPharmacy, "Add Pharmacy", JOptionPane.OK_CANCEL_OPTION, -1);

        if (option == JOptionPane.YES_OPTION) {
            Pharmacy pharmacy = providerEnterprise.getPharmacyDirectory().addPharmacy();
            if (pharmacyNameTextField.getText() == null || pharmacyAddressTextArea.getText() == null) {
                JOptionPane.showMessageDialog(null, "Please provide a valid entry");
                return;

            }
            try {
                String name = pharmacyNameTextField.getText();
                String address = pharmacyAddressTextArea.getText();
                pharmacy.setPharmacyName(name);
                pharmacy.setPharmacyAddress(address);
                pharmacy.setAdmin("Unassigned");
                pharmacy.setFinanaceUser("Unassigned");
                pharmacy.setInventoryUser("Unassigned");
                pharmacy.setOrderUser("Unassigned");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Please provide a valid entry");
                return;
            }
            JOptionPane.showMessageDialog(null, "Pharmacy details added successfully");
            refreshTable();
        }

    }//GEN-LAST:event_createPButtonActionPerformed

