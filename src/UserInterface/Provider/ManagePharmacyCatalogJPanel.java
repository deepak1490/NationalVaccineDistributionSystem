    private void delButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButtonActionPerformed
        // TODO add your handling code here:
        int row = pharmacyCatalog.getSelectedRow();
        int selectedRowCount = pharmacyCatalog.getSelectedRowCount();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a pharmacy");
            return;
        }
        if (selectedRowCount > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one pharmacy");
        }
        Pharmacy p = (Pharmacy) pharmacyCatalog.getValueAt(row, 0);
        providerEnterprise.getPharmacyDirectory().removePharmacy(p);
        refreshTable();
        JOptionPane.showMessageDialog(null, "Pharmacy deleted successfully");
    }//GEN-LAST:event_delButtonActionPerformed
