    private void orderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderButtonActionPerformed
        Network n = null;
        for (Network network : system.getNetworkDirectory().getNetworkList()) {
            for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (e == enterprise) {
                    n = network;
                    break;
                }
            }
        }
        ProvidersEnterprise e1 = null;
        for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
            if (e instanceof ProvidersEnterprise) {
                e1 = (ProvidersEnterprise) e;
            }
        }
        for (Pharmacy p : e1.getPharmacyDirectory().getPharmacyDirectory()) {
            if (p.getPharmacyName().equals(userAccount.getEmployee().getPharmacy().getPharmacyName())) {
                if (!p.getRegistrationStatus().equals("Approved")) {
                    JOptionPane.showMessageDialog(null, "Pharmacy has not been registered!");
                    return;
                }
            }
        }
        BrowseVaccinesJPanel register = new BrowseVaccinesJPanel(userProcessContainer, userAccount, organization, e1, system);
        userProcessContainer.add("OrderVaccineJPanel", register);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_orderButtonActionPerformed

