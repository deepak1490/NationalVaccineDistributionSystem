    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) reportVaccinesJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest r : userAccount.getWorkQueue().getWorkRequestList()) {
            if (r.getRequestType().equals("Report")) {
                ReportVaccineWorkRequest rv = (ReportVaccineWorkRequest) r;
                Object row[] = new Object[3];
                row[0] = rv.getVaccine();
                row[1] = rv.getVaccine().getVaccineName();
                row[2] = rv.getStatus();
                model.addRow(row);
            }
        }
    }

