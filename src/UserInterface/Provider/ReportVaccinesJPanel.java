    private void sendReportJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendReportJButtonActionPerformed
        // TODO add your handling code here:
        Vaccine vaccine = null;
        String vaccineName = (String) vaccineNameJComboBox.getSelectedItem();
        for (Vaccine v : system.getVaccineCatalog().getVaccineCatalog()) {
            if (v.getVaccineName().equals(vaccineName)) {
                vaccine = v;
            }
        }
        if (vaccine == null) {
            JOptionPane.showMessageDialog(null, "The vaccine is not present");
            return;
        }
        String report = reportJTextArea.getText();
        String message = messageJTextArea.getText();

        if (report.isEmpty() || message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please provide inputs for all fields");
            return;
        }

        ReportVaccineWorkRequest request = new ReportVaccineWorkRequest();
        WorkRequest r = request;
        request.setVaccine(vaccine);
        request.setMessage(message);
        request.setIssueReport(report);
        request.setSender(userAccount);
        request.setStatus("Reported to PHD");
        request.setRequestType("Report");

        Network n = null;
        Enterprise e1 = null;
        for (Network network : system.getNetworkDirectory().getNetworkList()) {
            for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (e == enterprise) {
                    n = network;
                    break;
                }
            }
        }
        for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
            if (e instanceof PHDEnterprise) {
                e1 = e;
            }
        }
        if (e1 != null) {
            e1.getWorkQueue().getWorkRequestList().add(r);
            userAccount.getWorkQueue().getWorkRequestList().add(r);
        }
        JOptionPane.showMessageDialog(null, "Report sent to PHD");
        populateTable();
    }//GEN-LAST:event_sendReportJButtonActionPerformed

