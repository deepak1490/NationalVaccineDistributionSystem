    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) reportJTable.getModel();
        model.setRowCount(0);
        for (WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()) {
            if (request.getRequestType().equals("Report")) {
                ReportVaccineWorkRequest report = (ReportVaccineWorkRequest) request;
                Object row[] = new Object[3];
                row[0] = report.getVaccine().getID();
                row[1] = report;
                row[2] = report.getStatus();
                model.addRow(row);
            }
        }
    }

