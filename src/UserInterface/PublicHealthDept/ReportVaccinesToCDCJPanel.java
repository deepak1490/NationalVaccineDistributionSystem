    private void viewJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = reportJTable.getSelectedRow();
        int rowC = reportJTable.getSelectedRowCount();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a report");
            return;
        }

        if (rowC > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one report");
            return;
        }

        ReportVaccineWorkRequest report = (ReportVaccineWorkRequest) reportJTable.getValueAt(selectedRow, 1);
        vaccineNameJTextField.setText(report.getVaccine().getVaccineName());
        messageJTextArea.setText(report.getMessage());
        reportJTextArea.setText(report.getIssueReport());

        populateTable();
    }//GEN-LAST:event_viewJButtonActionPerformed

    private void forwardJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forwardJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = reportJTable.getSelectedRow();
        int rowC = reportJTable.getSelectedRowCount();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a report");
            return;
        }

        if (rowC > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one report");
            return;
        }
        ReportVaccineWorkRequest report = (ReportVaccineWorkRequest) reportJTable.getValueAt(selectedRow, 1);
        WorkRequest request = report;
        request.setStatus("Sent to CDC");
        system.getWorkQueue().getWorkRequestList().add(request);
        JOptionPane.showMessageDialog(null, "Report forwarded to CDC");
        populateTable();
    }//GEN-LAST:event_forwardJButtonActionPerformed

